from day_09 import run_program


def test_intcode():
    assert (run_program([109, 1, 204, -1, 1001, 100, 1, 100, 1008, 100, 16, 101, 1006, 101, 0, 99], []) ==
            [109, 1, 204, -1, 1001, 100, 1, 100, 1008, 100, 16, 101, 1006, 101, 0, 99])
    assert (len(str(run_program([1102, 34915192, 34915192, 7, 4, 7, 99, 0], [])[0])) == 16)
    assert (run_program([104, 1125899906842624, 99], [])[0] == 1125899906842624)
